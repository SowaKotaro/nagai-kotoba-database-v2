# 読みのかな1文字を五十音の「行」(ア行〜ワ行・ン)と基本46字へ写像する値オブジェクト。
# 統計ページ(docs/stats.md)の §2 行×行ヒートマップと、頭文字・末尾文字の
# カバレッジ表記(44/46 字)に使う。
#
# 方針:
#   - しりとりの慣習に合わせ、濁音・半濁音は清音の行に含める(ガ行→カ行)。
#   - 小書きかな(ッ・ャ など)は対応する大書きの行に含める。
#   - ヴ はウの濁音としてア行、ヰ・ヱ はワ行に含める。
#   - ひらがな・カタカナは同一視する(照合順序 as_ci と同じ扱い)。
class KanaRow
  # 行の代表字 => その行に属する字(清音・濁音・半濁音・小書きを含む)。
  # 並び順は紙面の行順(ア→ワ、最後にン)として使う。
  ROWS = {
    "ア" => %w[ア イ ウ エ オ ヴ ァ ィ ゥ ェ ォ],
    "カ" => %w[カ キ ク ケ コ ガ ギ グ ゲ ゴ ヵ ヶ],
    "サ" => %w[サ シ ス セ ソ ザ ジ ズ ゼ ゾ],
    "タ" => %w[タ チ ツ テ ト ダ ヂ ヅ デ ド ッ],
    "ナ" => %w[ナ ニ ヌ ネ ノ],
    "ハ" => %w[ハ ヒ フ ヘ ホ バ ビ ブ ベ ボ パ ピ プ ペ ポ],
    "マ" => %w[マ ミ ム メ モ],
    "ヤ" => %w[ヤ ユ ヨ ャ ュ ョ],
    "ラ" => %w[ラ リ ル レ ロ],
    "ワ" => %w[ワ ヰ ヱ ヲ ヮ],
    "ン" => %w[ン]
  }.freeze

  # 行の並び(ア〜ワ、ン)。ヒートマップの行はン抜き、列はン込みで使う。
  ROW_KEYS = ROWS.keys.freeze

  CHAR_TO_ROW = ROWS.flat_map { |row, chars| chars.map { |char| [ char, row ] } }.to_h.freeze

  # 濁音・半濁音 => 清音。カバレッジ(基本46字)の判定に使う。
  VOICED_TO_SEION = {
    "ガ" => "カ", "ギ" => "キ", "グ" => "ク", "ゲ" => "ケ", "ゴ" => "コ",
    "ザ" => "サ", "ジ" => "シ", "ズ" => "ス", "ゼ" => "セ", "ゾ" => "ソ",
    "ダ" => "タ", "ヂ" => "チ", "ヅ" => "ツ", "デ" => "テ", "ド" => "ト",
    "バ" => "ハ", "ビ" => "ヒ", "ブ" => "フ", "ベ" => "ヘ", "ボ" => "ホ",
    "パ" => "ハ", "ピ" => "ヒ", "プ" => "フ", "ペ" => "ヘ", "ポ" => "ホ",
    "ヴ" => "ウ"
  }.freeze

  # 小書き => 大書き。
  SMALL_TO_LARGE = {
    "ァ" => "ア", "ィ" => "イ", "ゥ" => "ウ", "ェ" => "エ", "ォ" => "オ",
    "ッ" => "ツ", "ャ" => "ヤ", "ュ" => "ユ", "ョ" => "ヨ",
    "ヮ" => "ワ", "ヵ" => "カ", "ヶ" => "ケ"
  }.freeze

  # 歴史的仮名 => 現代の基本字。
  OLD_TO_MODERN = { "ヰ" => "イ", "ヱ" => "エ" }.freeze

  # 現代仮名の基本46字(五十音表の清音 + ン)。カバレッジ表記の分母。
  BASE_46 = %w[
    ア イ ウ エ オ カ キ ク ケ コ サ シ ス セ ソ タ チ ツ テ ト
    ナ ニ ヌ ネ ノ ハ ヒ フ ヘ ホ マ ミ ム メ モ ヤ ユ ヨ
    ラ リ ル レ ロ ワ ヲ ン
  ].freeze

  # 文字が属する行の代表字を返す(かな以外は nil)。
  def self.row(char)
    CHAR_TO_ROW[normalize(char)]
  end

  # 文字を基本46字へ畳んだ字を返す(基本46字に載らない文字は nil)。
  def self.base(char)
    folded = normalize(char)
    folded = SMALL_TO_LARGE[folded] || folded
    folded = VOICED_TO_SEION[folded] || folded
    folded = OLD_TO_MODERN[folded] || folded
    folded if BASE_46.include?(folded)
  end

  # NFKC 正規化(半角カナ・合成濁点を畳む)し、ひらがなをカタカナへ寄せる。
  def self.normalize(char)
    char.to_s.unicode_normalize(:nfkc).tr("ぁ-ゖ", "ァ-ヶ")
  end
  private_class_method :normalize
end
