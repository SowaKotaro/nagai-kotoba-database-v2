# 語種(word_origins)マスタを冪等に投入する。
# 「外来語」で束ねず言語ごとに切り分ける方針。混種語は語義に複数の語種を
# 紐づけて表現するため、ここには単一の語源としての値のみを並べる。
# 必要に応じて管理者が追加できる(開いた集合)。
WORD_ORIGINS = [
  "日本語",
  "中国語",
  "韓国語",
  "英語",
  "フランス語",
  "ドイツ語",
  "イタリア語",
  "スペイン語",
  "ポルトガル語",
  "ロシア語",
  "ギリシャ語",
  "ラテン語",
  "サンスクリット語"
].freeze

WORD_ORIGINS.each do |name|
  WordOrigin.find_or_create_by!(name: name)
end

puts "語種マスタを投入しました: #{WordOrigin.count} 件"
